public class CreateArrays {
    public static void main(String[] args) {
        int[] myIntArray;
        myIntArray = new int[5];
        
        myIntArray[0] = 1;
        myIntArray[1] = 2;
        myIntArray[2] = 3;
        myIntArray[3] = 4;
        myIntArray[4] = 5;

        System.out.print(myIntArray[0]);
        System.out.print(myIntArray[4]);
    }
}